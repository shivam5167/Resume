         <p class="h3 tbold">3. How to start writing an outstanding resume</p>
         <p class="h4">
            Most people will confirm that it is not easy to write an effective resume. One option of doing so is to use a resume builder but for those who prefer the option of writing the resume themselves, the best way to start is to create an inventory of work experience and personal background.<br><br>

            This entails documenting degrees/certifications and their affiliated institutions as well as companies worked for, job titles, and duties. The following questions can help a candidate from getting stuck when writing a resume for the first time.
         </p>

         <ol>
            <li>Have you been a supervisor and what has been your experience? Have you led any projects?</li>
            <li>Have managers and/or customers given you positive feedback?</li>
            <li>What accomplishments did you make in college? Are there clubs or organizations you joined that added value to you in the context of the job you are seeking?</li>
            <li>How would your former internship supervisors or employers describe your performance and work ethic?</li>
         </ol><br>

         <p class="h3 tbold">4. Importance of choosing the correct resume format </p>
         <p class="h4">
            The resume is normally representative of the job applicant and as such, communicates the applicant’s personality. The format is one of the aspects that employers use to decide whether to make a callback or eliminate the candidate.
            <br><br>
            The resume, being representative of the applicant, creates the first impression of the employer toward the jobseeker.  If the resume is awkwardly formatted and disorganized, the applicant has lesser chances of being selected. It is imperative to do it correctly the first time, as there are no do-overs.
            <br><br>

            One way of circumventing this problem is the use of resume templates. The template gives the applicant a preview of the resume as it unfolds.
         </p><br>

         <p class="h3 tbold">5. The correct length of a resume</p>
         <p class="h4">
            Traditionally, most jobseekers fit their education and work history in a single A4 page. However, this is not a convention regarding the length of a resume.<br><br>

            The resume may go well beyond a single page if:
         </p>
         <ol>
            <li>The jobseeker wants to express extensive experience in a field of interest to the employer; or</li>
            <li>The job seeker is a graduate hence need to describe in details his or her research to the employer.</li>
         </ol>
         <br>
         <p class="h4">However, the resume should not overflow to the third page. If the two criteria above do not apply, it is recommendable to limit the resume to a single page or alternatively go for the student resume.</p>
<br>
