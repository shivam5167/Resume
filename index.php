         <p class="h3 tbold">8. The Cover Letter</p>

         <p class="h4">
            The resume is normally accompanied by a cover letter. The purpose of a cover letter is to introduce the jobseeker to the potential employer.<br><br>

            A good cover letter complements the resume in convincing the employer to make a callback or an interview. It communicates in greater details the jobseeker’s experience and background so that the potential employer can understand the candidate in a much better way.<br><br>

            Experts recommend that each professional resume should be accompanied by a well-written cover letter. This is especially important for first time job applicants as it may supplement a thin resume.<br><br>

