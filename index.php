      <p class="h4">Statistics show that for most job adverts, the number of applications that the employers receive far exceeds the employers’ expectation. Given the huge workload, employers hardly take more than 6 to 7 seconds on each resume to decide whether or not an applicant is the right fit for the job. </p>

