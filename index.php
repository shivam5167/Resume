      <p class="h3 tbold">1. What makes an outstanding resume?</p>
      <p class="h4">
      Resumes are different in so many ways. There is no conventional way of crafting a professional and eye-catching resume. However, all great resumes share the following four qualities.</p>
      
      <dl>
         <dt class="h4 tbold">#Honesty</dt>
         <dd class="h4">Lying on one’s abilities, experience and background can attract the attention of the employer but is a very risky business. During an interview, the employer would most likely detect the lie and bar the applicant from all future job opportunities in the company. It is pointless to lie on the resume. Be honest.</dd>
        
         <dt class="h4 tbold">#Dynamic</dt>
         <dd class="h4">For every job posting, the applicant should tailor the resume based on the job description. Each job description has different keywords. It is imperative that the keywords appear in the resume particularly when explaining one’s work experience.</dd>

         <dt class="h4 tbold">#Clarity</dt>
         <dd class="h4">Keeping the resume focused, concise and clear significantly boosts the chances of being called for an interview.   A clear resume only highlights the most critical details thereby making it easier for the employer to identify the relevant information within the short duration s/he looks at the resume. It is unnecessary to include excessive details in the resume.</dd>


         <dt class="h4 tbold">#Digital</dt>
         <dd class="h4">A good resume should include the applicant’s online presence in platforms such as LinkedIn and Facebook. With such information, the employer has a greater understanding of the jobseeker.  However, the jobseeker must ensure that postings on LinkedIn and Facebook are appropriate and do not portray him/her in a bad light.</dd>
      </dl><br>

      <p class="h3 tbold">2. Important tips for writing an outstanding resume</p>
      <p class="h4">Generally speaking, no single resume guarantees an interview or a callback. Nonetheless, the following six tips can help a jobseeker compose an outstanding resume.</p>
      
      <dl>
         <dt class="h4 tbold">#Double-check to ensure all essential information is mentioned.</dt>
         <dd class="h4">Although different resumes have different content, there are some information that should not be left out, namely:

         <ol>
            <li>Applicant’s contact details</li>
            <li>The summary statement</li>
         </ol>
         </dd>

         <dt class="h4 tbold">#Educational qualifications.</dt>
         <dd class="h4">
         <ol>
            <li>Employment history</li>
            <li>Skills and abilities relevant to the job</li>
         </ol>
         </dd>
        
         <dt class="h4 tbold">#Proofread thoroughly</dt>
         <dd class="h4">To write an outstanding resume, it is imperative to proofread at least twice. Silly mistakes makes the jobseeker appear careless in the eye of the employer. Having a trusted peer with impeccable grammar check the resume is one way of ascertaining that there are no mistakes.</dd>

         <dt class="h4 tbold">#Start with critical resume section</dt>
         <dd class="h4">Impressive information ought to be placed at the top of the resume. Including relevant information first can catch the attention of the employer and reduce the chances of getting eliminated.</dd>


         <dt class="h4 tbold">#Organize the resume chronologically</dt>
         <dd class="h4">The reverse chronological order is mostly used when writing a resume. However, this does not apply when skills have to be emphasized due to absence of significant work history.</dd>

         <dt class="h4 tbold">#Quantify achievements</dt>
         <dd class="h4">Quantifying achievements at work using numbers and percentages increases the clarity and specificity of the resume. Using arbitrary terms to describe achievement does not bring out the essence of that achievement. For instance, “decreased employee turnover rate significantly” is not as effective as “deceased turnover rate by 25 percent in 6 months.”</dd>

         <dt class="h4 tbold">#Ensure verbs are strong throughout the resume</dt>
         <dd class="h4">The use of fresh language and strong verbs adds value to the resume. Using a term such as “spearheaded” in the place of “developed” in the context of a business strategy brings out a more favorable meaning.</dd>
      </dl>

